const mongoose = require("mongoose");

//Schema
const userSchema = new mongoose.Schema({
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  confirmPassword: {
    type: String,
    required: true,
  },
});

const userModel = mongoose.model("user", userSchema);
module.exports = userModel;
